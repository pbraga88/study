// Assignment operator (=)

#include <iostream>

using namespace std;

int main() {
    
    // Initialization
    int num1 {10};
    int num2 {20};
    
    // Assignment
    num1 = 100;
    
    cout << "num1 is " << num1 << endl;
    cout << "num2 is " << num2 << endl;

    cout << endl;
    return 0;
}

